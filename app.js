  /* ---------------- preferences application ---------------- */
  function applyPrefs() {
    document.documentElement.setAttribute("data-textsize", textSize());
    document.documentElement.setAttribute("lang", lang() === "ms" ? "ms" : "en");
    document.documentElement.setAttribute("data-theme", theme());
    var dark = theme() === "dark" ||
      (theme() === "auto" && window.matchMedia &&
       window.matchMedia("(prefers-color-scheme: dark)").matches);
    var meta = document.getElementById("meta-theme");
    if (meta) meta.setAttribute("content", dark ? "#1A1A1D" : "#F9F6F0");
  }

  /* ---------------- render dispatcher ---------------- */
  function render() {
    renderToken += 1;
    quizState = null;
    var current = route();
    switch (current.name) {
      case "home": paint("", homeHtml()); break;
      case "subjects": paint("subjects", subjectsPageHtml()); break;
      case "subject": paint("subjects", subjectPageHtml(current.parts[0] || "")); break;
      case "note": notePage(current.parts[0] || "", current.parts[1] || ""); break;
      case "quizzes": paint("quizzes", quizzesPageHtml()); break;
      case "quiz": quizPage(current.parts[0] || "", current.parts[1] || ""); break;
      case "papers": paint("papers", papersPageHtml()); break;
      case "paper": paperPage(current.parts[0] || "", current.parts[1] || ""); break;
      case "search": paint("", searchPageHtml(current.parts.join("/"))); break;
      default: paint("", notFoundHtml());
    }
  }

  /* ---------------- global event delegation ---------------- */
  document.addEventListener("click", function (event) {
    var target = event.target.closest ? event.target.closest("[data-action]") : null;
    if (!target) return;
    var action = target.getAttribute("data-action");
    if (action === "open-settings") { event.preventDefault(); openSettings(); }
    else if (action === "close-settings") { event.preventDefault(); closeSettings(); }
    else if (action === "set-lang") {
      setPref(PREF_LANG, target.getAttribute("data-value"));
      applyPrefs(); renderSettings(); render();
    }
    else if (action === "set-size") {
      setPref(PREF_SIZE, target.getAttribute("data-value"));
      applyPrefs(); renderSettings();
    }
    else if (action === "set-theme") {
      setPref(PREF_THEME, target.getAttribute("data-value"));
      applyPrefs(); renderSettings();
    }
    else if (action === "quiz-option") handleQuizOption(target);
    else if (action === "toggle-key") toggleAnswerKey(target);
    else if (action === "print") window.print();
    else if (action === "retry-quiz") render();
  });
  document.addEventListener("submit", function (event) {
    var form = event.target.closest ? event.target.closest("form[data-search]") : null;
    if (!form) return;
    event.preventDefault();
    var input = form.querySelector('input[name="q"]');
    var query = input ? input.value.trim() : "";
    if (query) location.hash = "#/search/" + encodeURIComponent(query);
  });
  document.addEventListener("keydown", function (event) {
    if (event.key === "Escape") closeSettings();
  });

  /* ---------------- boot ---------------- */
  function init() {
    appEl = document.getElementById("app");
    layerEl = document.getElementById("settings-layer");
    if (!appEl || !layerEl) {
      console.error("[cairn] required containers missing");
      return;
    }
    applyPrefs();
    window.addEventListener("hashchange", function () {
      closeSettings();
      render();
    });
    loadManifest().then(render);
  }
  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", init);
  } else {
    init();
  }
