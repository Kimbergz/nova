  function splitAnswerKey(md) {
    var match = /^##\s+(answer key|skema jawapan)\s*$/im.exec(md);
    if (!match) return { body: md, key: "" };
    return {
      body: md.slice(0, match.index),
      key: md.slice(match.index + match[0].length)
    };
  }

