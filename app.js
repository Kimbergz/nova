  function stripFrontmatter(src) {
    var match = /^﻿?\s*---[^\n]*\n[\s\S]*?\n---[^\n]*\n?/.exec(src);
    return match ? src.slice(match[0].length) : src;
  }
  function fixRelativeMedia(html, baseDir) {
    var tpl = document.createElement("template");
    tpl.innerHTML = html;
    var images = tpl.content.querySelectorAll("img[src]");
    for (var i = 0; i < images.length; i++) {
      var src = images[i].getAttribute("src") || "";
      if (!/^(https?:|data:|\/|#)/i.test(src)) {
        images[i].setAttribute("src", baseDir + "/" + src);
      }
    }
    return tpl.innerHTML;
  }
  function renderMarkdown(src, baseDir) {
    if (!window.marked || !window.DOMPurify) {
      console.error("[cairn] marked/DOMPurify not loaded");
      return "<p>" + esc(t("loadErrorHint")) + "</p>";
    }
    var raw = window.marked.parse(src, { gfm: true, breaks: true, async: false });
    var clean = window.DOMPurify.sanitize(raw, { USE_PROFILES: { html: true } });
    return fixRelativeMedia(clean, baseDir);
  }

  /* ---------------- routing ---------------- */
  function route() {
    var hash = location.hash.replace(/^#\/?/, "");
    if (!hash) return { name: "home", parts: [] };
    var parts = hash.split("/").map(function (part) {
      try { return decodeURIComponent(part); } catch (err) { return part; }
    });
    return { name: parts[0] || "home", parts: parts.slice(1) };
  }
